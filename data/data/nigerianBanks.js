// Helper function to get bank name from bank code
export const getBankNameFromCode = (bankCode) => {
  const bank = nigerianBanks.find(bank => bank.code === bankCode);
  return bank ? bank.name : bankCode; // Return the code if bank not found
};

// Helper function to get bank code from bank name (for backward compatibility)
export const getBankCodeFromName = (bankName) => {
  const bank = nigerianBanks.find(bank => bank.name === bankName);
  return bank ? bank.code : bankName; // Return the name if bank not found
};

